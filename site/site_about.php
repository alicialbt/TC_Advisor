  <h2>Le projet</h2>
    </br>
    <p>TCAdvisor est un site fait pour que chaque étudiant du département <I>Télecommunications</I> de l’<I>INSA Lyon</I> qui a pour souhait de partir en échange ou en stage à l’étranger puisse accéder facilement à une base de données d’étudiants déjà partis.</p>
      <ul>
        <p>Chaque étudiant voulant partir en échange peut ainsi accéder :</p> 
          <li>- aux <b>contrats d’études</b> liés à l’université qui l’intéresse</li>
          <li>- aux <b>cours intéressants</b> et <b>langues</b> de ceux-ci</li>
          <li>- aux <b>contacts</b> qui sont partis là-bas (nom, adresse-mail et téléphone si celui-ci est renseigné)</li> 
          <li>- s’informer sur la <b>ville</b> liée à l’université</li>
      </ul>
    <p>Mais il peut aussi chercher des contacts d’étudiants partis en stages à l’étrangers dans une entreprise spécifique.</p>

